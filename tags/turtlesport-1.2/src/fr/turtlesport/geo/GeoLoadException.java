package fr.turtlesport.geo;

/**
 * @author Denis Apparicio
 * 
 */
public class GeoLoadException extends Exception {

  public GeoLoadException() {
    super();
  }

  public GeoLoadException(String message, Throwable cause) {
    super(message, cause);
  }

  public GeoLoadException(String message) {
    super(message);
  }

  public GeoLoadException(Throwable cause) {
    super(cause);
  }

}
